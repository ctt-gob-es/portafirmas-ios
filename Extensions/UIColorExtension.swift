extension UIColor {
    class func segmentedControl(_ alpha: CGFloat = 1.0) -> UIColor {
        return  UIColor.init(red: 105.0/255.0, green: 25.0/255.0, blue: 31.0/255.0, alpha: 1.0)
    }
}
